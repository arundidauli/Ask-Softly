function buildQuestionsForRelationship(relationship) {
  const toneGroups = QUESTION_BANK[relationship] || {};
  return Object.entries(toneGroups).flatMap(([tone, items]) =>
    items.map((text) => ({ text, tones: [tone] }))
  );
}

const hinglishQuestions = Object.keys(QUESTION_BANK).reduce((acc, relationship) => {
  acc[relationship] = buildQuestionsForRelationship(relationship);
  return acc;
}, {});

window.QUESTIONS = {
  hinglish: hinglishQuestions,
  english: hinglishQuestions
};
